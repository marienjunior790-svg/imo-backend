export function resolveRolePermissions(role: string): string[] {
  const entry = ROLE_PERMISSION_MATRIX[role as SystemRole];
  if (!entry) return [];
  if (entry === 'ALL') return [...ALL_PERMISSION_KEYS];
  return entry;
}
